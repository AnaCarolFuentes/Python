"""
Tipo de tipagem = Dinâmica/Forte
O Python já sabe qual é o tipo de dado, por isso não é preciso colocar
Strings -> qualquer texto que está dentro de aspas
"""
#Aspas simples
print('Name : Carol')

#Aspas duplas
print('Name : Simon')

#Caractere de escape -> quando coloca uma \, o interpretador pula o próximo caractere
print("Ana \" Carolina")

#r
print(r"Ana \" Carolina")
print("Ana 'Carolina'") #Imprime as aspas duplas

